package com.modern.chating.modal;

public class ModalArray {
    public String title;
    public String message;
    public int icon;
    public String urlImage;
    public String category;

    public ModalArray(String title, String message, int icon, String urlImage, String category) {
        this.title = title;
        this.message = message;
        this.icon = icon;
        this.urlImage = urlImage;
        this.category = category;
    }
}
